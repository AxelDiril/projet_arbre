    public void afficher(LinkedHashMap <Integer, M_Utilisateur> lhmLesUtilisateurs,
            M_Utilisateur unUtilisateurConnecte){
        this.lhmLesUtilisateurs = lhmLesUtilisateurs;
        this.unUtilisateurConnecte = unUtilisateurConnecte;
        
        this.modeEdit(false);
        
        aff_cms();
        
        dtm_tb_util = new DefaultTableModel();
        dtm_tb_util = (DefaultTableModel) tb_util.getModel();
        dtm_tb_util.setRowCount(lhmLesUtilisateurs.size());
        
        // Remplir les utilisateurs
        
        iLigne = 0;
        
        for(Integer uneCle : lhmLesUtilisateurs.keySet()){
            M_Utilisateur unUtilisateur = lhmLesUtilisateurs.get(uneCle);
            
            dtm_tb_util.setValueAt(unUtilisateur.getIdUtilisateur(), iLigne, 0);
            dtm_tb_util.setValueAt(unUtilisateur.getLogin(), iLigne, 1);
            
            iLigne++;
        }
        
        iLigne = 0;
        
        // Séléction par défaut et remplissage automatique
        
        tb_util.setRowSelectionInterval(0, 0);
        remplirChamps();
        
        this.setLocationRelativeTo(null);
        this.setSize(500, 520);
        this.setVisible(true);
    }
    
