    private void bt_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_supprimerActionPerformed
        if(iCle == unUtilisateurConnecte.getIdUtilisateur()){
            JOptionPane.showMessageDialog(this, "Vous ne pouvez pas supprimer votre propre compte.",
                "Erreur",
                JOptionPane.ERROR_MESSAGE);
        }
        else{
            int reponse = JOptionPane.showConfirmDialog(this,
                    "Etes-vous sûr de vouloir de supprimer cet utilisateur ?", "Suppression",
                    JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if(reponse == JOptionPane.YES_OPTION){
                try {
                    leControl.delete_Compte(iCle);
                    JOptionPane.showMessageDialog(null, "Le compte a bien été supprimé.",
                            "Mise à jour", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    Logger.getLogger(V_Utilisateurs.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_bt_supprimerActionPerformed

