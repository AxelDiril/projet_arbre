    private void bt_enregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_enregistrerActionPerformed

        // Valoriser les paramètres

        String strMail = ed_mail.getText();
        String strLogin = ed_login.getText();

        // Vérifier si tous les champs sont remplis

        if(!strMail.isEmpty() && !strLogin.isEmpty()){
            // Vérifie si on est en modification
            
            if(bIsModif == true){
                try {
                    leControl.update_Compte(iCle, strLogin, strMail);
                    JOptionPane.showMessageDialog(null, "Le compte a bien été mis à jour.",
                            "Mise à jour", JOptionPane.INFORMATION_MESSAGE);
                    leControl.aff_V_Utilisateurs();
                } catch (SQLException ex) {
                    Logger.getLogger(V_Utilisateurs.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_bt_enregistrerActionPerformed

